package com.spark.easyleave;

public class Leave {
    String fromDate;
    String toDate;
    String reason;
    String supEmail;
    String addressForComm;

    public String getFromDate() {
        return fromDate;
    }

    public void setFromDate(String fromDate) {
        this.fromDate = fromDate;
    }

    public String getToDate() {
        return toDate;
    }

    public void setToDate(String toDate) {
        this.toDate = toDate;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getSupEmail() {
        return supEmail;
    }

    public void setSupEmail(String supEmail) {
        this.supEmail = supEmail;
    }

    public String getAddressForComm() {
        return addressForComm;
    }

    public void setAddressForComm(String addressForComm) {
        this.addressForComm = addressForComm;
    }
}
